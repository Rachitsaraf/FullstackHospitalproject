                            <p>In a groundbreaking global medical achievement, Dr. Mohit Bhandari has performed the
                                world’s first transcontinental tele-robotic surgery, operating the SSI Mantra Surgical
                                Robot from IRCAD France. On the other hand, Mohak Bariatrics & Robotics in Indore was
                                where the patient and the robotic system were located. This remarkable feat not only
                                marks a technological breakthrough but also redefines the accessibility of expert
                                surgical care across borders. Powered by Indian innovation, this surgery indicates the
                                endless possibilities of remote, precision-based healthcare.</p>

                            <b>A Surgical Bridge Between Continents</b>
                            <p>From a high-tech console at IRCAD France, Dr. Mohit Bhandari remotely controlled the SSI
                                Mantra robot, installed at Mohak Bariatric and Robotics, Indore, to perform a complex
                                gastric bypass surgery with precision, stability, and complete patient safety. This is
                                the first time in medical history that a robotic surgery has been successfully conducted
                                across continents using a system developed in India.</p>

                            <b>Dr. Mohit Bhandari, President of IRCAD India and Founder & Director of Mohak Bariatric
                                and Robotic Surgery Centre, Indore, said:</b>

                            <p>“Performing a complex gastric bypass remotely, across continents, marks a transformative
                                moment in surgical science. It’s not just about technological capability; it’s about
                                bridging gaps in access to quality healthcare. The success of the SSI Mantra system
                                proves that the future of safe, precise, and inclusive surgery is here, and it’s being
                                shaped in India.”</p>

                            <b>SSI Mantra: An Indian Innovation Changing Global Healthcare</b>

                            <p>The SSI Mantra Surgical System, developed by SS Innovations, is the first completely
                                indigenous robotic surgery platform designed in India. Designed to be cost-effective,
                                scalable, and highly precise, it is now validated at the global level through this
                                historic surgery.
                                With real-time responsiveness and zero lag, the platform performed seamlessly in an
                                transcontinental setting, proving its readiness for the world stage.</p>

                            <b>Dr. Mohit Bhandari: Redefining What’s Possible</b>

                            <p>Known throughout the world for his groundbreaking work in robotic and bariatric surgery,
                                Dr. Mohit Bhandari has continuously driven innovations in minimally invasive techniques.
                                With over 25,000 surgeries to his name and numerous surgical firsts in India, this
                                latest success adds to his legacy as a medical visionary and change maker.</p>


