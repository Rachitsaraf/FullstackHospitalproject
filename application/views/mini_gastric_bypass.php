                     <h2 class="fw-bold mb-3">HOW IT WORKS</h2>

                     <p style="margin-bottom: 18px; line-height: 1.8;">
                        In the first part of <strong>mini gastric bypass surgery</strong>, the stomach is divided
                        and a small tube of stomach is created which becomes the pouch. This limits the amount of
                        food that can be eaten at any one time.
                     </p>

                     <p style="margin-bottom: 18px; line-height: 1.8;">
                        Next, the surgeon brings up a loop of bowel (about 200–300 cm long) and joins it to the
                        lower part of the stomach pouch. This connection, called an “anastomosis,” allows food
                        to pass from the small pouch into the small bowel where it mixes with digestive juices.
                     </p>

                     <p style="margin-bottom: 18px; line-height: 1.8;">
                        As a result, 2–3 meters of the small intestine is bypassed before absorption occurs.
                        With fewer calories absorbed, weight loss becomes more effective.
                     </p>

                     <h2 class="fw-bold mt-4 mb-3">RESULTS</h2>

                     <p style="margin-bottom: 18px; line-height: 1.8;">
                        The <strong>Mini Gastric Bypass (MGBP)</strong> works in three ways:  
                        by limiting food intake, by causing malabsorption, and by altering gut hormones
                        responsible for hunger and satiety.
                     </p>

